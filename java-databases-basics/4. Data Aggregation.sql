-- Problem 1. Departments Info

SELECT
	department_id,
    COUNT(*)
FROM employees
GROUP BY department_id
ORDER BY department_id;



-- ----------------------------------------------------------------------------------------------------------------------------------
-- ----------------------------------------------------------------------------------------------------------------------------------
-- ----------------------------------------------------------------------------------------------------------------------------------
-- Problem 2. Average Salary

SELECT
	department_id,
    ROUND(AVG(salary), 2)
FROM employees
GROUP BY department_id
ORDER BY department_id;



-- ----------------------------------------------------------------------------------------------------------------------------------
-- ----------------------------------------------------------------------------------------------------------------------------------
-- ----------------------------------------------------------------------------------------------------------------------------------
-- Problem 3. Minimum Salary

SELECT
	department_id,
    ROUND(MIN(salary), 2) as 'min'
FROM employees
GROUP BY department_id
HAVING `min` > 800;



